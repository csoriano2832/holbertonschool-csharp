using System;

namespace MyMath
{
    /// <summary>Provides basic math operations.</summary>
    public class Operations
    {
        /// <summary>Adds two numbers.</summary>
        /// <param name="a">A negative or positive number.</param>
        /// <param name="b">A second negative or positive number.</param>
        /// <returns>The sum of these numbers.</returns>
        public static int Add(int a, int b)
        {
            return (a + b);
        }
    }
}
